    public void mostrarDatos() {

    modelo = (DefaultTableModel) tblTipoUsuario.getModel();

    modelo.setRowCount(0);

    for (TipoUsuario t : dao.listar()) {

        Object[] fila = new Object[2];

        fila[0] = t.getIdTipo();
        fila[1] = t.getNombreTipo();

        modelo.addRow(fila);
        
    }

}
