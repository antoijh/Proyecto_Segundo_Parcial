    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        tipo.setNombreTipo(txtNombreTipo.getText());

if(dao.guardar(tipo)){

    JOptionPane.showMessageDialog(this,"Guardado correctamente");

    txtNombreTipo.setText("");

    mostrarDatos();

}else{

    JOptionPane.showMessageDialog(this,"Error al guardar");

}
    
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if(txtIdTipo.getText().isEmpty()){
        JOptionPane.showMessageDialog(this, "Ingrese un ID");
        return;
    }

    int id = Integer.parseInt(txtIdTipo.getText());

    TipoUsuario t = dao.buscar(id);

    if(t.getIdTipo() != 0){

        txtNombreTipo.setText(t.getNombreTipo());

    }else{

        JOptionPane.showMessageDialog(this,"Registro no encontrado");

    }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        if(txtIdTipo.getText().isEmpty()){

        JOptionPane.showMessageDialog(this,"Busca un registro primero");

        return;

    }

    tipo.setIdTipo(Integer.parseInt(txtIdTipo.getText()));
    tipo.setNombreTipo(txtNombreTipo.getText());

    if(dao.actualizar(tipo)){

        JOptionPane.showMessageDialog(this,"Actualizado correctamente");

        mostrarDatos();

    }else{

        JOptionPane.showMessageDialog(this,"Error al actualizar");

    }

    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if(txtIdTipo.getText().isEmpty()){

        JOptionPane.showMessageDialog(this,"Ingrese un ID");

        return;

    }

    int id = Integer.parseInt(txtIdTipo.getText());

    if(dao.eliminar(id)){

        JOptionPane.showMessageDialog(this,"Registro eliminado");

        limpiar();

        mostrarDatos();

    }else{

        JOptionPane.showMessageDialog(this,"No se pudo eliminar");

    }

    }//GEN-LAST:event_btnEliminarActionPerformed

